package main
import "fmt"



func main() {
	
	m := 32
	n := 90

	a := (m == n)
	b := (m <= n)
	c := (m >= n)
	d := (m != n)
	e := (m < n)
	f := (m > n)
	

	fmt.Println(a)
	fmt.Println(b)
	fmt.Println(c)
	fmt.Println(d)
	fmt.Println(e)
	fmt.Println(f)
	
}



